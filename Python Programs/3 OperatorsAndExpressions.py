x = 5
y = 2

#Exponention
print("Exponention:",x**y)

#Multiplication
print("Multiplication:",x*y)

#Division
print("Division:",x/y)

#Int division
print("Int division:",x//y)

#Remainder (modulus)
print("Remainder (modulus):",x%y)

#Addition
print("Addition:",x+y)

#Subtraction
print("Subtraction:",x-y)

#Operations of equal priority are performed in left to right order 
#Consistent with BODMAS/BIDMAS rule

#Formatting Numbers
x1 = 1.96
print ("Rounding: %.1f "%x1)
